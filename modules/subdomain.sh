# Subdomain Module

# Subdomain-related functions for PostgreSQL server setup

# Function to set up subdomain routing
setup_subdomain_routing() {
    log "Setting up subdomain routing"
    
    # Install Nginx if not already installed
    if ! command_exists "nginx"; then
        log "Installing Nginx for subdomain routing"
        apt-get install -y nginx
    fi
    
    # Set up Nginx template for database subdomains
    setup_nginx_template
    
    # Copy subdomain script to system location
    copy_subdomain_script
    
    # Set up automatic subdomain creation
    setup_automatic_subdomain_creation
}

# Function to set up Nginx template for database subdomains
setup_nginx_template() {
    log "Setting up Nginx template for database subdomains"
    
    # Create Nginx sites directory
    mkdir -p /etc/nginx/sites-available
    
    # Create template file
    cat > /etc/nginx/sites-available/db-subdomain-template << 'EOF'
server {
    listen 80;
    listen [::]:80;
    server_name {{SUBDOMAIN}}.{{DOMAIN_SUFFIX}};

    access_log /var/log/nginx/{{SUBDOMAIN}}.access.log;
    error_log /var/log/nginx/{{SUBDOMAIN}}.error.log;

    location / {
        proxy_pass http://localhost:8080;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        
        # Add custom header for database routing
        proxy_set_header X-Database-Name {{DATABASE}};
    }

    # Additional security headers
    add_header X-Content-Type-Options nosniff;
    add_header X-Frame-Options SAMEORIGIN;
    add_header X-XSS-Protection "1; mode=block";
    add_header Strict-Transport-Security "max-age=31536000; includeSubDomains" always;
}
EOF
}

# Function to copy subdomain script to system location
copy_subdomain_script() {
    log "Installing database subdomain script"
    
    # Create scripts directory
    mkdir -p /usr/local/bin
    
    # Copy script
    cp "$(dirname "$0")/scripts/create_db_subdomain.sh" /usr/local/bin/
    chmod +x /usr/local/bin/create_db_subdomain.sh
}

# Function to set up automatic subdomain creation
setup_automatic_subdomain_creation() {
    log "Setting up automatic subdomain creation for databases"
    
    # Check if PostgreSQL is running
    if service_running "postgresql"; then
        # Set up trigger
        /usr/local/bin/create_db_subdomain.sh setup-trigger
        
        # Create subdomains for existing databases
        /usr/local/bin/create_db_subdomain.sh create-all
    else
        log "WARNING: PostgreSQL is not running, skipping subdomain setup"
    fi
    
    # Restart Nginx to apply changes
    if service_running "nginx"; then
        log "Restarting Nginx to apply changes"
        systemctl restart nginx
    else
        log "WARNING: Nginx is not running, skipping restart"
    fi
}

# Function to update PostgreSQL subdomain mapping
update_pg_hostname_map() {
    local db_name="$1"
    local subdomain="$2"
    
    log "Updating PostgreSQL hostname mapping for $db_name -> $subdomain"
    
    # Get the paths
    local PG_CONF_DIR="/etc/postgresql/$PG_VERSION/main"
    local MAP_FILE="$PG_CONF_DIR/pg_hostname_map.conf"
    
    # Check if the map file exists
    if [ ! -f "$MAP_FILE" ]; then
        log "Creating PostgreSQL hostname map file: $MAP_FILE"
        
        # Create directory if it doesn't exist
        mkdir -p "$PG_CONF_DIR"
        
        # Create the initial mapping file
        cat > "$MAP_FILE" << EOF
# Map hostnames to database names
# Format: database_name subdomain.domain.com
# Example: demo demo.example.com

# Dynamic entries will be added by the DBHub.cc system
EOF
        
        # Set proper permissions
        chown postgres:postgres "$MAP_FILE"
        chmod 640 "$MAP_FILE"
        
        # Update postgresql.conf to use the hostname map
        local PG_CONF_FILE="$PG_CONF_DIR/postgresql.conf"
        if [ -f "$PG_CONF_FILE" ]; then
            grep -q "^host_name_map" "$PG_CONF_FILE" || echo "host_name_map = '$MAP_FILE'" >> "$PG_CONF_FILE"
            grep -q "^check_hostname" "$PG_CONF_FILE" || echo "check_hostname = on" >> "$PG_CONF_FILE"
        else
            log "WARNING: PostgreSQL configuration file not found: $PG_CONF_FILE"
        fi
    fi
    
    # Update the mapping file
    if grep -q "^${db_name} " "$MAP_FILE"; then
        # Update existing entry
        sed -i "s|^${db_name} .*$|${db_name} ${subdomain}.${DOMAIN_SUFFIX}|" "$MAP_FILE"
    else
        # Add new entry
        echo "${db_name} ${subdomain}.${DOMAIN_SUFFIX}" >> "$MAP_FILE"
    fi
    
    # Restart PostgreSQL to apply changes if running
    if pg_isready -q; then
        log "Reloading PostgreSQL configuration"
        sudo -u postgres psql -c "SELECT pg_reload_conf();"
    fi
    
    log "PostgreSQL hostname mapping updated successfully"
}

